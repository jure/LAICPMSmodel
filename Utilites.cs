        public static string array2string (int[,] array, int n, int m)
        {
          string arrayString = "";

          for (int i = 0; i < n; i++) 
          {
                for (int j = 0; j < m; j++) 
                { 
                    arrayString += Math.Round((decimal)array[i, j],3).ToString();
                    if (j == m - 1) { arrayString += '\n'; }
                    else { arrayString += ", ";}
                }
            }
          return arrayString; 
        }

        public static double getRSD(List<double> doubleList)
        {
            double average = doubleList.Average();
            double sumOfDerivation = 0;

            doubleList.ForEach(v => sumOfDerivation += Math.Pow(v, 2));

            double sumOfDerivationAverage = sumOfDerivation / doubleList.Count;

            return (Math.Sqrt(sumOfDerivationAverage - Math.Pow(average, 2)))*100/average;
        }

        public static double getSNR(List<double> doubleList)
        {
            double average = doubleList.Average();
            double sumOfDerivation = 0;

            doubleList.ForEach(v => sumOfDerivation += Math.Pow(v, 2));

            double sumOfDerivationAverage = sumOfDerivation / doubleList.Count;

            return (average/Math.Sqrt(sumOfDerivationAverage - Math.Pow(average, 2)));
        }

        public static List<float[]> parseCSV(string path, bool resource)
        {
            List<float[]> parsedData = new List<float[]>();

            try
            {
                using (StringReader readFile = new StringReader(path))
                {
                    string line;
                    string[] row;

                    while ((line = readFile.ReadLine()) != null)
                    {
                        row = line.Split(',');
                        float[] rowFloat = new float[row.Length];
                        for (int i = 0; i < rowFloat.Length; i++)
                        {
                            rowFloat[i] = float.Parse(row[i]);
                        }
                        parsedData.Add(rowFloat);
                    }
                }
            }
            catch (Exception e)
            {
                System.Windows.Forms.MessageBox.Show(e.Message);
            }

            return parsedData;
        }
