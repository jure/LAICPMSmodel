        //Another method for making a 2D array from an image, ** FASTER **
        public static int[,] imageTo2DArray(ref Bitmap imageIn)
        {
            try
            {
                int height = imageIn.Height;
                int width = imageIn.Width;
                int[,] Values = new int[height, width];

                BitmapData bmData = imageIn.LockBits(new Rectangle(0, 0, width, height), ImageLockMode.ReadOnly, PixelFormat.Format24bppRgb);

                int stride = bmData.Stride;
                System.IntPtr Scan0 = bmData.Scan0;

                unsafe
                {
                    byte* p = (byte*)(void*)Scan0;

                    int nOffset = stride - width * 3; //how wide each row is

                    for (int y = 0; y < height; ++y) //nested, sacn all x for each y
                    {
                        for (int x = 0; x < width; ++x) //scan through x axis and record values
                        {
                            Values[y, x] = (p[2] + p[1] + p[0]) / 3; //if i = 2 red, i = 1 green, i = 0 blue
                            
                            p += 3;
                        }
                        p += nOffset;
                    }
                }
                imageIn.UnlockBits(bmData);
                return Values;
            }
            catch (OutOfMemoryException ex)
            {
                throw ex;
            }
            finally
            {
                // Clean up?
            }

        }

        // my array to byte array
        public static byte[] ArrayFromMyArray(ref double[,] array, ref List<float[]> colorMap)
        {
            byte[] byteArray = new byte[array.GetLength(0) * array.GetLength(1) * 4];
            double max = MaxArrayValue(array);
            int k = 0;
            if (colorMap != null)
            {
                for (int i = 0; i < array.GetLength(0); i++)
                {
                    for (int j = 0; j < array.GetLength(1); j++)
                    {
                        int scaledValue = (int)Math.Floor(65535 * array[i, j] / max);
                        byteArray[k + 3] = 255;
                        byteArray[k + 2] = (byte)(colorMap[scaledValue][0] * 255);
                        byteArray[k + 1] = (byte)(colorMap[scaledValue][1] * 255);
                        byteArray[k + 0] = (byte)(colorMap[scaledValue][2] * 255);
                        k += 4;
                       
                    }
                }
            }
            else
            {
                for (int i = 0; i < array.GetLength(0); i++)
                {
                    for (int j = 0; j < array.GetLength(1); j++)
                    {
                        byteArray[k + 3] = 255;
                        byteArray[k] = byteArray[k + 1] = byteArray[k + 2] = (byte)Math.Floor((double)array[i, j] * 255 / max);
                        k += 4;
                    }
                }
            }
            return byteArray;
        }
        // byte array to bitmap
        public static Bitmap BitmapFromArray(byte[] bitmapData, double[,] finalMatrix)
        {
            // MemoryStream ms = new MemoryStream(bitmapData);
            // return new Bitmap(ms);

            //Here create the Bitmap to the know height, width and format
            Bitmap bmp = new Bitmap(finalMatrix.GetLength(1), finalMatrix.GetLength(0), PixelFormat.Format32bppArgb);
            //Create a BitmapData and Lock all pixels to be written
            BitmapData bmpData = bmp.LockBits(
                                 new Rectangle(0, 0, bmp.Width, bmp.Height),
                                 ImageLockMode.WriteOnly, bmp.PixelFormat);

            //Copy the data from the byte array into BitmapData.Scan0
            Marshal.Copy(bitmapData, 0, bmpData.Scan0, bitmapData.Length);

           

            //Unlock the pixels
            bmp.UnlockBits(bmpData);


            //Return the bitmap
            return bmp;
        }

        public static Bitmap ResizeBitmap(Bitmap b, int newWidth, int newHeight)
        {
            Bitmap result = new Bitmap(newWidth, newHeight);
            using (Graphics g = Graphics.FromImage((Image)result))
            {
                g.SmoothingMode = SmoothingMode.None;
                g.InterpolationMode = InterpolationMode.NearestNeighbor;
                g.PixelOffsetMode = PixelOffsetMode.Half;
                g.DrawImage(b, 0, 0, newWidth, newHeight);
            }
            return result;
        }

