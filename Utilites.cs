        public static int saveMatrix(double[,] array, string filename)
        {
            FileStream stream = new FileStream(filename, FileMode.Create);
            StreamWriter writer = new StreamWriter(stream);

            int sizeHorizontal = array.GetLength(1);
            int sizeVertical = array.GetLength(0);

            for (int i = 0; i < sizeVertical; i++)
            {
                string line = "";

                for (int j = 0; j < sizeHorizontal; j++)
                {
                    if (j == 0)
                    {
                        line = array[i, j].ToString();
                    }
                    else
                    {
                        line = line + "," + array[i, j].ToString();
                    }
                }

                writer.WriteLine(line);
            }

            writer.Close();
            stream.Close();

            return 0;

        }

